export const getProducts = (filter = {}) => {
let list = [...PRODUCTS]
if (filter.category) list = list.filter(p => p.category === filter.category)
if (filter.featured) list = list.filter(p => p.featured)
if (filter.search) {
const q = filter.search.toLowerCase()
list = list.filter(p =>
p.name.toLowerCase().includes(q) ||
p.description.toLowerCase().includes(q) ||
(p.tags||[]).some(t => t.includes(q))
)
}
if (filter.minPrice) list = list.filter(p => p.price >= filter.minPrice)
if (filter.maxPrice) list = list.filter(p => p.price <= filter.maxPrice)
if (filter.sort === 'price-asc')  list.sort((a,b) => a.price - b.price)
if (filter.sort === 'price-desc') list.sort((a,b) => b.price - a.price)
if (filter.sort === 'name-asc')   list.sort((a,b) => a.name.localeCompare(b.name))
if (filter.sort === 'newest')     list.sort((a,b) => b.id - a.id)
return list
}

